#include "functions.hpp"

double Functions::add(double num1, double num2) {
	return num1 + num2;
}

double Functions::subtract(double num1, double num2) {
	return num1 - num2;
}

double Functions::multiply(double num1, double num2) {
	return num1 * num2;
}

double Functions::divide(double num1, double num2) {
	return num1 / num2;
}